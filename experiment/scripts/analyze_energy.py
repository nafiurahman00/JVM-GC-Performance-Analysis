def plot_energy_per_joule_normalized(df: pd.DataFrame, title: str,
                                     output_path: Path):
    """
    Bar chart comparing energy per benchmark normalized to G1GC baseline.
    Values < 1 = more energy-efficient than G1GC.
    """
    setup_light_theme()

    gcs_present = [gc for gc in GC_ORDER if gc in df["gc_name"].unique()
                   and gc != "epsilon"]
    if "g1gc" not in gcs_present:
        return

    agg = df.groupby("gc_name")["energy_pkg_joules"].mean()
    baseline = agg.get("g1gc", None)
    if baseline is None or baseline == 0:
        return

    labels, values, colors = [], [], []
    for gc in gcs_present:
        if gc not in agg.index:
            continue
        ratio = agg[gc] / baseline
        labels.append(GC_STYLES.get(gc, (gc,))[0])
        values.append(float(ratio))
        colors.append(GC_STYLES.get(gc, (gc, "#94a3b8"))[1])

    fig, ax = plt.subplots(figsize=(max(9, len(labels) * 1.4), 6))

    bars = ax.bar(range(len(labels)), values, color=colors,
                  edgecolor=BAR_EDGE, linewidth=0.8, alpha=0.88)
    ax.axhline(1.0, color=TEXT_COLOR, linewidth=1.2, linestyle="--",
               label="G1GC baseline (1.0)", alpha=0.7)

    for bar, val in zip(bars, values):
        ax.text(bar.get_x() + bar.get_width() / 2,
                bar.get_height() + 0.01,
                f"{val:.2f}×", ha="center", va="bottom",
                fontsize=9, fontweight="bold")

    ax.set_xticks(range(len(labels)))
    ax.set_xticklabels(labels, fontsize=10, rotation=20, ha="right")
    ax.set_ylabel("Energy Relative to G1GC (1.0 = same)", fontsize=11)
    ax.set_title(title, fontsize=13, fontweight="bold")
    ax.legend(fontsize=9)
    ax.grid(axis="y", linestyle="--", alpha=0.3)

    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  [CHART] {output_path}")


def plot_energy_pareto(df: pd.DataFrame, title: str, output_path: Path):
    """
    Pareto scatter: X = avg energy (J), Y = avg wall-time (ms).
    Each point = one GC config (averaged across benchmarks).
    Points in the bottom-left corner are Pareto-optimal.
    Pareto frontier is drawn.
    """
    setup_light_theme()

    gcs_present = [gc for gc in GC_ORDER if gc in df["gc_name"].unique()
                   and gc != "epsilon"]
    agg = df[df["gc_name"].isin(gcs_present)].groupby("gc_name").agg(
        energy=("energy_pkg_joules", "mean"),
        walltime=("wall_time_ms", "mean"),
    )

    xs = [agg.loc[gc, "energy"] for gc in gcs_present if gc in agg.index]
    ys = [agg.loc[gc, "walltime"] for gc in gcs_present if gc in agg.index]
    gcs = [gc for gc in gcs_present if gc in agg.index]

    if not xs:
        return

    fig, ax = plt.subplots(figsize=(9, 7))
    ax.grid(linestyle="--", alpha=0.3)

    for gc, x, y in zip(gcs, xs, ys):
        label, color = GC_STYLES.get(gc, (gc, "#94a3b8"))
        ax.scatter([x], [y], color=color, s=140, edgecolors=BAR_EDGE,
                   linewidths=0.8, zorder=3)
        ax.annotate(label, (x, y), fontsize=8, color=color,
                    xytext=(6, 4), textcoords="offset points")

    # Draw approximate Pareto frontier (lower-left hull)
    pts = sorted(zip(xs, ys))
    frontier = []
    min_y = float("inf")
    for px, py in pts:
        if py < min_y:
            frontier.append((px, py))
            min_y = py
    if len(frontier) > 1:
        fx, fy = zip(*frontier)
        ax.step(fx, fy, where="post", color=ACCENT, linewidth=1.5,
                linestyle="--", label="Pareto frontier", alpha=0.8)

    ax.set_xlabel("Avg Energy Consumption (Joules) — lower is better",
                  fontsize=11)
    ax.set_ylabel("Avg Wall-Clock Time (ms) — lower is better",
                  fontsize=11)
    ax.set_title(title + "\n(Bottom-left = best on both dimensions)",
                 fontsize=12, fontweight="bold")
    ax.legend(fontsize=8)

    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  [CHART] {output_path}")


