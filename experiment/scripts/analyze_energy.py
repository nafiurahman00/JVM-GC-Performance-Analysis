def parse_energy_file(filepath: Path) -> dict | None:
    """
    Parse a single *_energy.txt file produced by run_energy_benchmarks.sh.

    Returns a dict with keys:
        status, method, energy_pkg_joules, energy_cores_joules,
        energy_uncore_joules, wall_time_ms, gc_name, benchmark, suite
    or None if the file cannot be parsed / status is FAILED.
    """
    record = {}
    try:
        with open(filepath, "r") as f:
            for line in f:
                line = line.strip()
                if "=" not in line:
                    continue
                key, _, val = line.partition("=")
                record[key.strip()] = val.strip()
    except (OSError, UnicodeDecodeError):
        return None

    if record.get("status") != "SUCCESS":
        return None

    # Convert numeric fields
    for numeric_key in ("energy_pkg_joules", "energy_cores_joules",
                        "energy_uncore_joules", "wall_time_ms"):
        try:
            record[numeric_key] = float(record.get(numeric_key, 0))
        except ValueError:
            record[numeric_key] = 0.0

    # Derive total energy (pkg encompasses cores + uncore on most CPUs,
    # so we use pkg as the primary metric)
    record["total_energy_joules"] = record["energy_pkg_joules"]

    return record


def load_all_results(results_dir: Path) -> pd.DataFrame:
    """
    Walk the results/energy/ tree and collect all *_energy.txt files
    into a DataFrame.
    """
    rows = []

    for energy_file in sorted(results_dir.rglob("*_energy.txt")):
        record = parse_energy_file(energy_file)
        if record is not None:
            rows.append(record)

    if not rows:
        print("[!] No successful energy results found in", results_dir)
        sys.exit(1)

    df = pd.DataFrame(rows)

    # Ensure column ordering and types
    df["wall_time_sec"] = df["wall_time_ms"] / 1000.0
    df["avg_power_watts"] = df.apply(
        lambda r: r["total_energy_joules"] / r["wall_time_sec"]
        if r["wall_time_sec"] > 0 else 0.0,
        axis=1
    )

    return df


