def generate_csv(df: pd.DataFrame, output_path: Path):
    """
    Write a tidy CSV summarizing energy per (suite, benchmark, gc_name).
    """
    cols = [
        "suite", "benchmark", "gc_name", "method",
        "energy_pkg_joules", "energy_cores_joules", "energy_uncore_joules",
        "total_energy_joules", "avg_power_watts",
        "wall_time_ms", "wall_time_sec",
    ]
    out = df[cols].sort_values(["suite", "benchmark", "gc_name"])
    out.to_csv(output_path, index=False, float_format="%.4f")
    print(f"  [CSV] {output_path}  ({len(out)} rows)")


