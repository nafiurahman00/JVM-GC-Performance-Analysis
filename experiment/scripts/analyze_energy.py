def plot_grouped_energy_bar(df: pd.DataFrame, title: str,
                            output_path: Path):
    """
    Grouped bar chart: benchmarks on x-axis, one bar per GC,
    y-axis = Total Energy (Joules).
    """
    setup_light_theme()

    benchmarks = sorted(df["benchmark"].unique())
    gc_list = [g for g in GC_ORDER if g in df["gc_name"].unique()]

    if not benchmarks or not gc_list:
        print(f"  [SKIP] Not enough data for chart: {title}")
        return

    n_bench = len(benchmarks)
    n_gc = len(gc_list)

    fig, ax = plt.subplots(figsize=(max(12, n_bench * 2.0), 7))

    x = np.arange(n_bench)
    bar_width = 0.72 / n_gc
    offsets = np.linspace(-(n_gc - 1) * bar_width / 2,
                           (n_gc - 1) * bar_width / 2, n_gc)

    for i, gc_name in enumerate(gc_list):
        label, color = GC_STYLES.get(gc_name, (gc_name, "#94a3b8"))
        values = []
        for bench in benchmarks:
            row = df[(df["benchmark"] == bench) & (df["gc_name"] == gc_name)]
            val = row["total_energy_joules"].values[0] if len(row) > 0 else 0
            values.append(val)

        bars = ax.bar(
            x + offsets[i], values, bar_width * 0.88,
            label=label, color=color,
            edgecolor=BAR_EDGE, linewidth=0.6,
            alpha=0.92, zorder=3
        )

        # Glow effect — faint wider bar behind each bar
        ax.bar(
            x + offsets[i], values, bar_width * 0.88,
            color=color, alpha=0.08, zorder=2
        )

        # Value labels
        for bar, val in zip(bars, values):
            if val > 0:
                fontsize = 7 if n_bench > 8 else 8
                if val >= 1000:
                    txt = f"{val:,.0f}"
                elif val >= 10:
                    txt = f"{val:.1f}"
                else:
                    txt = f"{val:.2f}"
                ax.text(
                    bar.get_x() + bar.get_width() / 2,
                    bar.get_height(),
                    txt, ha="center", va="bottom",
                    fontsize=fontsize, color=TEXT_COLOR,
                    fontweight="bold", rotation=35
                )

    ax.set_xlabel("Benchmark", fontsize=13, fontweight="bold")
    ax.set_ylabel("Total Energy Consumed (Joules)", fontsize=13,
                   fontweight="bold")
    ax.set_title(title, fontsize=16, fontweight="bold", pad=18)
    ax.set_xticks(x)
    ax.set_xticklabels(benchmarks, rotation=30, ha="right", fontsize=10)
    ax.legend(loc="upper right", framealpha=0.92, fontsize=11)
    ax.grid(axis="y", alpha=0.3, linestyle="--")
    ax.set_axisbelow(True)

    # Subtle accent line at y=0
    ax.axhline(y=0, color=ACCENT, linewidth=0.4, alpha=0.5)

    plt.tight_layout()
    plt.savefig(output_path, bbox_inches="tight", pad_inches=0.25)
    plt.close()
    print(f"  [CHART] {output_path}")


def plot_energy_vs_walltime(df: pd.DataFrame, title: str,
                            output_path: Path):
    """
    Scatter plot: wall-clock time vs energy, one marker style per GC,
    labeled by benchmark name.
    """
    setup_light_theme()

    gc_list = [g for g in GC_ORDER if g in df["gc_name"].unique()]
    if not gc_list:
        return

    fig, ax = plt.subplots(figsize=(10, 7))

    markers = ["o", "s", "D"]
    for i, gc_name in enumerate(gc_list):
        label, color = GC_STYLES.get(gc_name, (gc_name, "#94a3b8"))
        subset = df[df["gc_name"] == gc_name]

        ax.scatter(
            subset["wall_time_sec"], subset["total_energy_joules"],
            label=label, color=color, marker=markers[i % len(markers)],
            s=90, alpha=0.88, edgecolors="white", linewidths=0.6,
            zorder=3
        )
        # Glow
        ax.scatter(
            subset["wall_time_sec"], subset["total_energy_joules"],
            color=color, marker=markers[i % len(markers)],
            s=220, alpha=0.10, zorder=2
        )

        # Annotate with benchmark names
        for _, row in subset.iterrows():
            ax.annotate(
                row["benchmark"],
                (row["wall_time_sec"], row["total_energy_joules"]),
                textcoords="offset points", xytext=(6, 6),
                fontsize=7, color=TEXT_COLOR, alpha=0.75
            )

    ax.set_xlabel("Wall-Clock Time (seconds)", fontsize=13, fontweight="bold")
    ax.set_ylabel("Total Energy (Joules)", fontsize=13, fontweight="bold")
    ax.set_title(title, fontsize=15, fontweight="bold", pad=15)
    ax.legend(loc="upper left", framealpha=0.92, fontsize=11)
    ax.grid(True, alpha=0.3, linestyle="--")
    ax.set_axisbelow(True)

    plt.tight_layout()
    plt.savefig(output_path, bbox_inches="tight", pad_inches=0.25)
    plt.close()
    print(f"  [CHART] {output_path}")


