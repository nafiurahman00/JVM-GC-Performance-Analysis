def main():
    print("=" * 70)
    print("  Green Computing: JVM GC Energy Consumption Analysis")
    print("=" * 70)

    os.makedirs(RESULTS_DIR, exist_ok=True)
    combined_dir = ENERGY_DIR / "combined"
    os.makedirs(combined_dir, exist_ok=True)

    # ── Load data ────────────────────────────────────────────────────────
    print("\n[1/4] Loading energy results...")
    df = load_all_results(RESULTS_DIR)
    print(f"  Loaded {len(df)} successful runs")
    print(f"  Suites:     {sorted(df['suite'].unique())}")
    print(f"  Benchmarks: {sorted(df['benchmark'].unique())}")
    print(f"  GCs:        {sorted(df['gc_name'].unique())}")
    print(f"  Method:     {df['method'].unique().tolist()}")

    # ── Generate CSV ─────────────────────────────────────────────────────
    print("\n[2/4] Generating energy summary CSV...")
    generate_csv(df, CSV_OUTPUT)

    # ── Combined chart (all suites) ──────────────────────────────────────
    print("\n[3/4] Generating combined energy charts...")
    plot_grouped_energy_bar(
        df,
        "Energy Consumption by GC and Benchmark (All Suites)",
        combined_dir / "energy_consumption.png"
    )

    plot_energy_vs_walltime(
        df,
        "Energy vs. Wall-Clock Time by GC",
        combined_dir / "energy_vs_walltime.png"
    )

    plot_energy_per_joule_normalized(
        df,
        "Energy Efficiency Relative to G1GC (All Suites)",
        combined_dir / "energy_normalized_vs_g1gc.png"
    )
    plot_energy_pareto(
        df,
        "Energy vs Speed Pareto — All GC Configs",
        combined_dir / "energy_pareto_all.png"
    )

    # ── Per-suite charts ─────────────────────────────────────────────────
    print("\n[4/4] Generating per-suite charts...")
    for suite in sorted(df["suite"].unique()):
        suite_df = df[df["suite"] == suite]
        suite_title = suite.replace("_", " ").title()
        suite_dir = ENERGY_DIR / suite
        os.makedirs(suite_dir, exist_ok=True)

        plot_grouped_energy_bar(
            suite_df,
            f"{suite_title}: Energy Consumption by GC",
            suite_dir / "energy_bars.png"
        )

        plot_avg_power(
            suite_df,
            f"{suite_title}: Average Power Draw by GC",
            suite_dir / "energy_avg_power.png"
        )

        plot_energy_vs_walltime(
            suite_df,
            f"{suite_title}: Energy vs. Wall-Clock Time",
            suite_dir / "energy_scatter.png"
        )

        plot_energy_per_joule_normalized(
            suite_df,
            f"{suite_title}: Energy Relative to G1GC",
            suite_dir / "energy_normalized.png"
        )

        plot_energy_pareto(
            suite_df,
            f"{suite_title}: Energy vs Speed Pareto",
            suite_dir / "energy_pareto.png"
        )

    # ── Summary ──────────────────────────────────────────────────────────
    total_charts = sum(1 for _ in ENERGY_DIR.rglob("*.png"))
    print()
    print("=" * 70)
    print(f"  Analysis Complete!")
    print(f"  CSV:    {CSV_OUTPUT}")
    print(f"  Charts: {total_charts} PNG files in {ENERGY_DIR}")
    print("=" * 70)

    # ── Quick console summary ────────────────────────────────────────────
    print("\n── Energy Summary (Joules) ──\n")
    pivot = df.pivot_table(
        index="benchmark", columns="gc_name",
        values="total_energy_joules", aggfunc="first"
    )
    # Reorder columns
    pivot = pivot[[c for c in GC_ORDER if c in pivot.columns]]
    pivot.columns = [GC_STYLES.get(c, (c,))[0] for c in pivot.columns]
    print(pivot.to_string(float_format="{:.2f}".format))
    print()


if __name__ == "__main__":
    main()
